     * @param  \App\Models\Order  $order
     * @return void
     */
    public function updated(Order $order): void
    {
        if ($order->status == Order::ORDER_STATUS_PAID) {
            $point = ($order->total*5)/100;
            DB::table('users')->where('id', $order->user_id)
                ->update([
                    'point' => floor($point)
                ]);
        }
    }

